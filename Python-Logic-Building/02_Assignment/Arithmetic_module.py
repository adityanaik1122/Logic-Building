def Add(Value1, Value2):
    Ans = Value1 + Value2
    return Ans

def Sub(Value1, Value2):
    Ans = Value1 - Value2
    return Ans

def Mult(Value1, Value2):
    Ans = Value1 * Value2
    return Ans


def Div(Value1, Value2):
    Ans = Value1 / Value2
    return Ans
